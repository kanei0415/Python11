print("num1 - num2 =", (num1 - num2))
print("num1 * num2 =", (num1 * num2))
print("num1 / num2 =", (num1 / num2))
print("num1 ** num2 =", (num1 ** num2))
print("num1 % num2 =", (num1 % num2))
print("num1 // num2 =", (num1 // num2))

# 나머지 연산 %
#  - 두 수를 나누고, 나머지 값만 사용
#  - 12를 3으로 나눈 나머지가 0이면 12는 3의 배수
#  - 어떤 수를 2로 나눈 나머지가 0이면 2의 배수(짝수), 나머지가 1이면(홀수)
print()

